    540h  en contrat de professionnalisation (donc en alternance) pour les demandeurs d’emplois). Durée totale : 9 à 11 mois
    340h en période de professionnalisation pour les salariés. Durée totale : de 3 à 6 mois (à définir selon les besoins)
